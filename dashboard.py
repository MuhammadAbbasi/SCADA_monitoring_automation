# JavaScript for AJAX polling
js_code = """
<script>
let lastUpdated = '';

function updateDashboard() {
    fetch('http://localhost:8000/dashboard_data.json')
        .then(response => response.json())
        .then(data => {
            if (data.last_updated !== lastUpdated) {
                lastUpdated = data.last_updated;
                // Trigger Streamlit rerun by updating a hidden element or using Streamlit's rerun
                window.location.reload();  // Simple way to refresh
            }
        })
        .catch(error => console.log('Error fetching data:', error));
}

setInterval(updateDashboard, 5000);  // Check every 5 seconds
</script>
"""

st.components.v1.html(js_code, height=0)

# Keep the Python polling as fallback
